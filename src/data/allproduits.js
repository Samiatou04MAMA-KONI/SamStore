// Fonctions utilitaires
export const getRandomProducts = (count = 12) => {
  const shuffled = [...allProducts].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getProductsWithDiscount = () => {
  return allProducts.filter(product => product.discount > 0)
                    .sort((a, b) => b.discount - a.discount);
};

export const getBestRatedProducts = () => {
  return allProducts.filter(product => product.rating >= 4.5)
                    .sort((a, b) => b.rating - a.rating);
};

export const getNewArrivals = () => {
  return allProducts.filter(product => product.badge === "Nouveau" || product.badge === "Nouvelle Collection");
};